  validate(sections){
    validations = {};
    for(let p in sections){
      validations[p] = {};
      // console.log(p);
      // console.log();
      let validCounter = 0;
      let valid = true;
      sections[p].forEach(function(e){
        let value;
        if($(e).is("textarea")){
          value = $(e).text() != ''? $(e).text() : null;
        }
        else if($(e).attr("type")=="checkbox" || $(e).attr("type")=="radio"){
          value = $(e).attr("checked") != "checked"? null : $(e).attr("checked");
        }
        else{
          value = $(e).val() != ''? $(e).val() : null;
        }

        validations[p][e] = value;
        if(value != null){
          validCounter++;
        }

        // console.log($(e).attr('name')+" = "+value);
      });
      if(validCounter <= 0){
        valid = false;
      }
      validations[p]["valid"] = valid;

      sections[p] = valid;
      // console.log();
    }
    let validForm = true;
    for(let y in sections){
      if(!sections[y]){
        validForm = false;
      }
    }
    validations["valid"] = validForm;
    this.validation = validations;
    return validForm;
  }

